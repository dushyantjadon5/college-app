    private void deleteStudMarksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteStudMarksActionPerformed
        Connection con = myConnection.getConnection();
        PreparedStatement ps;
        
        try {
            
            ps = con.prepareStatement("DELETE FROM `studsubjects_&_marks` WHERE `studCollId` = ?");
            ps.setString(1, studMarksId.getText());
            
            if(ps.executeUpdate() != 0) {
                JOptionPane.showMessageDialog(null, "Deleted!!");
            } else {
                JOptionPane.showMessageDialog(null, "No Data found!!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(studentMarks.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteStudMarksActionPerformed

