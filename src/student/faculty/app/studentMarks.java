    public boolean isUsernameExist(String un) {
        
        boolean uExist = false;
        Connection con = myConnection.getConnection();
        PreparedStatement ps;
        ResultSet rs;
        
        try {
            ps = con.prepareStatement("SELECT * FROM `studsubjects_&_marks` WHERE `studCollId` = ?");
            ps.setString(1, studMarksId.getText());
            
            
            rs = ps.executeQuery();
            
            if(rs.next()) {
                uExist = true;
            } 
            
        } catch (SQLException ex) {
            Logger.getLogger(loginPortal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return uExist;
    }
    
    
    
